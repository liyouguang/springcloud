package com.lyg.cloud.model;

public class PayInfo {
	
	private String id;
	private String payName;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPayName() {
		return payName;
	}

	public void setPayName(String payName) {
		this.payName = payName;
	}

}
