package com.lyg.cloud.model;

public class SocialInfo {
	
	private String id;
	private String socialName;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSocialName() {
		return socialName;
	}

	public void setSocialName(String socialName) {
		this.socialName = socialName;
	}

}
